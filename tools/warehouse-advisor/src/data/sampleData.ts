export const throughputHistory = Array.from({ length: 24 }, (_, i) => {
  const hour = i.toString().padStart(2, '0') + ':00'
  const base = i >= 6 && i <= 22 ? 80 + Math.sin((i - 6) * 0.4) * 40 : 10
  const noise = (Math.random() - 0.5) * 12
  const actual = Math.max(0, Math.round(base + noise))
  return {
    hour,
    actual,
    capacity: 220,
    target: 185,
  }
})
